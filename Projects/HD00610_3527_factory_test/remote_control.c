void Read_Blob_Parse(const ATT_TABLE_TYPE *table, uint16_t offset)
{
    DEBUG_LOG_STRING("READ HANDLE: %d \r\n", table->handle);
    uint8_t senddata[251] = {ATTOP_READ_BLOB_RESPONSE};

    if (table->handle == 64937) {
        memset((void *)&senddata[1], 0, 250);
        ATT_sendNotify(64937, &senddata[1], 69);
        ATT_Send(senddata, 251);
    }

    else {
        uint16_t sendlen = ((table->dataLen - offset) >= ((HREADW(mem_mtu_size)) - 1)) ? ((HREADW(mem_mtu_size)) - 1) : (table->dataLen - offset);

        memcpy(senddata + 1, table->dataPtr + offset, sendlen);
        ATT_Send(senddata, sendlen + 1);
    }
}

void Read_Parse(const ATT_TABLE_TYPE *table)
{
    DEBUG_LOG_STRING("READ HANDLE: %d \r\n", table->handle);
    uint8_t senddata[251] = {ATTOP_READ_RESPONSE};
    
    if (table->handle == 64679)
    {
        if (flash_record_exist(ir_learn_tag)) {
            senddata[1] = 0;
            ATT_Send(senddata, 2);
        }
        else {
            senddata[1] = 0x81;
            flash_read(ir_learn_tag, &senddata[2], sizeof(ir_learn_tag_buf));
           
            ATT_Send(senddata, sizeof(ir_learn_tag_buf) + 2);
        }
    }
    else if(table->handle == 64937)
    {
        memset((void *)&senddata[1], 0, 250);

        if (flash_record_exist(ir_learn_tag)) {
            uint8_t sendbuf[] = {0x00, 0x00, 0x00, 0x00, 0x30, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x04, 0x02, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x08, 0x06, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x0D, 0x04, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x10, 0x09, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x13, 0x03, 0x00, 0x00, 0x00, 0x00};
            ATT_sendNotify(64937, sendbuf, 69);
        }
        else {
            ATT_sendNotify(64937, &senddata[1], 69);
        }
        ATT_Send(senddata, 251);
    }

    else {
        uint16_t sendlen = (table->dataLen >= (HREADW(mem_mtu_size)) - 1)? ((HREADW(mem_mtu_size)) - 1) : table->dataLen;

        memcpy((void *)&senddata[1], table->dataPtr, sendlen);
        ATT_Send(senddata, sendlen+1);
    }
}

void Write_DataParse(const ATT_TABLE_TYPE *table, uint8_t *data, uint8_t len)
{
    DEBUG_LOG_STRING("WRITE HANDLE: %d  LEN: %d\r\n", table->handle, len);
    factory_WriteDataParse(table->handle, data, len);
    if (table->handle == 110)
    {
        if (!memcmp(MIC_CLOSE, data, 1)) {
            if (voice_status.mic_open_flag) {
                mic_close();
            }
        }
    }

    else if (table->handle == 64676 && !voice_status.mic_open_flag)
    {
        if (ir_learn_buf_cur_offset < ir_learn_buf_len) {
            memcpy(get_ir_learn_data_buf_addr(ir_learn_buf_cur_offset), data, len);
            ir_learn_buf_cur_offset += len;
        }
        if (ir_learn_buf_cur_offset >= ir_learn_buf_len && ir_learn_buf_cur_offset < IR_LEARN_DATA_BUFSIZE) {
            ir_learn_data_fill();
            app_sleep_lock_set(APP_LOCK, false);
            DEBUG_LOG_STRING("IR LEARN DONE \r\n");
        }
    }
    else if (table->handle == 64679)
    {
        if (len == 1 && data[0] == 0x10) {
            ir_learn_flag = true;
            if (app_sleep_check()) {
                voice_status.mic_open_flag = false;
                ir_learn_reinit();
            }
            app_sleep_lock_set(APP_LOCK, true);
        }
        else if (ir_learn_flag && len == 22 && data[0] == 0x02 && data[1] == 0x01)
        {
            ir_learn_flag = false;
            ir_learn_buf_len = (data[len - 1] << 8) + data[len - 2];

            if (ir_learn_buf_len > IR_LEARN_DATA_BUFSIZE) {
                return;
            }
            else {
                ir_learn_buf_cur_offset = 0;
                memcpy(ir_learn_tag_buf, (void *)(data+2), sizeof(ir_learn_tag_buf));
                ir_learn_data_clr();
                flash_write(ir_learn_tag, ir_learn_tag_buf, sizeof(ir_learn_tag_buf), DATA_INF);
            }
        }
        else if(len == 18 && data[0] == 0x10 && data[1] == 0x00) {
            ir_learn_data_clr();
        }
    }
    
    else if (table->handle == 64932)
    {
        if (data[0] == 0x80) {
            ATT_sendNotify(64934, (void *)cmd80_buf, 46);
        }
        else if (data[0] == 0x81)
        {
            if (flash_record_exist(ir_learn_tag)) {
                ATT_sendNotify(64934, (void *)ir_learn_start, sizeof(ir_learn_start));
            }
            else {
                ATT_sendNotify(64934, (void *)ir_learn_stop, sizeof(ir_learn_stop));
            }
        }
        else if (data[0] == 0x82) {
            s_4a18_buf_offset = 0;
            s_4a18_send();
            
            if (app_sleep_check()) {
                software_timer_start(SYSTEM_CURRENT_CLOCK, TIMER_UNIT_MS);
            }
            app_sleep_lock_set(APP_LOCK, true);
        }
    }

    else if (table->handle == 65028)
    {
        if ((data[0] == 0x80) && isalnum(data[1]) && isalnum(data[2]) && isalnum(data[3]) &&
            isalnum(data[4]) && isalnum(data[5]) && isalnum(data[6]) && isalnum(data[7]) &&
            isalnum(data[8]))
        {
            ATT_sendNotify(65028, chrctr_cfbf_01_value, 1);
        }

        if ((data[0] == 0x81) && (data[1] == 0x00))
        {
            ota_cnt = 0;
            ota_buf[0] = (uint8_t)ota_cnt;
            ota_buf[1] = (uint8_t)(ota_cnt++ >> 8);
            DEBUG_LOG_STRING("65033 handle\r\n");
            ATT_sendNotify(65033, ota_buf, 2);
        }
        else
        {
            DEBUG_LOG_STRING("65033:0x%x 0x%x len:%d\r\n", data[0], data[1], len);
        }
    }
    else if (table->handle == 65031)
    {
    }
    else if (table->handle == 65033)
    {
        ota_buf[0] = (uint8_t)ota_cnt;
        ota_buf[1] = (uint8_t)(ota_cnt++ >> 8);
        ATT_sendNotify(65033, ota_buf, 2);
    }

    else if (table->handle == OTA_WRITE_HANDLE)
    {
        if (app_sleep_check()) {
            software_timer_start(SYSTEM_CURRENT_CLOCK, TIMER_UNIT_MS);
        }
        app_sleep_lock_set(APP_LOCK, true);
        app_sleep_lock_set(ADV_LOCK,false);
        OS_ENTER_CRITICAL();
        if(OTA_EVT_HANDLE(len, data) != OTA_NO_ERROR) {
            OTA_RESET();
            app_sleep_lock_set(APP_LOCK, false);
        }
        OS_EXIT_CRITICAL();
    }
    else if (table->dataLen >= len) {
        memcpy((void *)table->dataPtr, (void *)data, len);
    }
    else {
        DEBUG_LOG_STRING("ATT WRITE DATA ERROR, HANDLE: %d, WRITE LEN: %d \r\n", table->handle, len);
    }
}

