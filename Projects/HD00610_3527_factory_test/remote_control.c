static void stop_adv(void)
{
    if (bt_check_le_connected()) return;
    else
    {
        if(bt_get_le_state() == BLE_DIRECT_ADV) {
            IPC_TxControlCmd(BT_CMD_STOP_DIRECT_ADV);
        }
        else {
            IPC_TxControlCmd(BT_CMD_STOP_ADV);
        }

        if(Bt_CheckIsPaired()) {
            bt_set_le_state(BLE_PAIRED);
        }
        else {
            bt_set_le_state(BLE_IDLE);
        }
    }
}

static void start_adv(enum advType type, uint16_t adv_interval, bool timeout)
{
    struct bt_le_adv_param bt_adv_param;
    bt_adv_param.Type = type;
    bt_adv_param.adv_max_interval = adv_interval;
    if (bt_adv_param.Type == ADV_TYPE_NOMAL)
    {
        if(bt_get_le_state() == BLE_DIRECT_ADV) {
            stop_adv();
        }           
        bt_set_le_state(BLE_ADV);
        bt_start_le_adv(&bt_adv_param, adv_data_buf, sizeof(adv_data_buf));
        if (timeout) {
            app_sleep_lock_set(ADV_LOCK, true);
            app_sleep_timer_set(SHUTDOWN_TIME);
        }
    }
    else if (bt_adv_param.Type == ADV_TYPE_DIRECT)
    {
        bt_set_le_state(BLE_DIRECT_ADV);
        bt_start_le_adv(&bt_adv_param, NULL, 0);
        if (timeout) {
            app_sleep_lock_set(ADV_LOCK, true);
            app_sleep_timer_set(DIRECT_ADV_TIME);
        }
    }
}

static void key_pressed_handle(void)
{
    bool le_connected_state = bt_check_le_connected();
    
    if (key_pressed_num == 0) {
        key_pressed_time = 0;
        led_off(LED_2);
    }
    else if (key_pressed_num == 1)
    {
        if (keynum == Home_Keynum)
        {
            DEBUG_LOG_STRING("Home_Key: key_pressed_time[%d] \r\n", key_pressed_time);
            if (key_pressed_time < 12) {
                key_pressed_time++;
                swtimer_restart(key_pressed_timernum);
            }
            else if (key_pressed_time >= 12)
            {
                adv_flag = true;

                Bt_ClearRemoteDevInfo();
                Bt_ClearDeviceNvdataInfo();
                start_adv(ADV_TYPE_NOMAL,0x10,false);
                app_sleep_lock_set(ADV_LOCK, false);
                led_on(LED_2, 100, 90000);
            }
        }
    }
    else if (key_pressed_num == 2)
    {
        if (keynum == Right_Keynum && keynum_second == Back_Keynum && le_connected_state)
        {
            DEBUG_LOG_STRING("RESET TV: key_pressed_time[%d] \r\n", key_pressed_time);
            if (key_pressed_time < 10) {
                key_pressed_time++;
                swtimer_restart(key_pressed_timernum);
            }
            else if (key_pressed_time >= 10)
            {
                uint8_t sendbuf[] = {0x4F, 0x00, 0X00};

                ATT_sendNotify(94, sendbuf, 3);
                sendbuf[1] = 0XF1;
                ATT_sendNotify(94, sendbuf, 3);
            }
        }
    }
    else if (key_pressed_num == 3)
    {
        if (keynum == Menu_Keynum && keynum_second == Left_Keynum && keynum_third == Back_Keynum)
        {
            DEBUG_LOG_STRING("RESET RCU: key_pressed_time[%d] \r\n", key_pressed_time);
            if (key_pressed_time < 10) {
                key_pressed_time++;
                swtimer_restart(key_pressed_timernum);
            }
            else if (key_pressed_time >= 10)
            {
                flash_Erase(ir_learn_tag, sizeof(addr_inf_t));
                ir_learn_data_clr();
                flash_Erase(first_done, sizeof(addr_inf_t));
                first_pair = false;
                flash_write(first_done, (uint8_t *)&first_pair, sizeof(first_pair), STATE_INF);

                if (bt_check_le_connected()) {
                    uint8_t sendbuf[] = {0x50, 0xF1, 0X00};
                    ATT_sendNotify(94, sendbuf, 3);
                }
                Bt_ClearRemoteDevInfo();
                Bt_ClearDeviceNvdataInfo();
                start_adv(ADV_TYPE_NOMAL,0x10,true);
            }
        }
        else if (keynum == Menu_Keynum && keynum_second == Right_Keynum && keynum_third == Back_Keynum && !le_connected_state)
        {
            DEBUG_LOG_STRING("HCI_DTM_TEST: key_pressed_time[%d] \r\n", key_pressed_time);
            if (key_pressed_time < 3) {
                key_pressed_time++;
                swtimer_restart(key_pressed_timernum);
            }
            else if (key_pressed_time >= 3)
            {
                tx_freq_flag = true;
                stop_adv();
                app_sleep_lock_set(ADV_LOCK, false);
                hci_freq_tx_test();
            }
        }
    }
}

